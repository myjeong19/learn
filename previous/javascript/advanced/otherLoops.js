// forEach()
const numbers = [5, 10, 15];
numbers.forEach(number => console.log(number));

// for
for (let i = 0; i < 2; i++) {
  console.log(i);
}

// while
let i = 0;
while (i < 3) {
  i++;
  console.log(i);
}

// for of
const text = 'Hello';
for (const character of text) {
  console.log(character);
}
